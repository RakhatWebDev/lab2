n = int(input()) # Бүтін сан енгіземіз
m = 1
while m < n: #Цикл ашу арқылы сол бүтін санымыз 1 ден үлкен болса
    if(m < n): # егер санымыз 1  ден үлкен болса
        if (m * 3 > n): #  ары қарай сол енгізген санымыз 3тің еселігі ма соны тексереміз
            break
        else: # егер ол сан n-нен яғни енгізген саннан асып кетсе 
            m = m * 3 # сол соңғы шығарған 3-ке еселік санның өзін шығарып береді
            print(m)